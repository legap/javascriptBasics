var trainsOperational = 8;
var trainNumber;

// looping with ascending numbers
for (trainNumber = 1; trainNumber <= trainsOperational; trainNumber++) {
    console.log("Train #" + trainNumber + " is running.");
}

// looping with descending numbers
for (trainNumber = 5; trainNumber > trainsOperational; trainNumber--) {
    console.log("Train #" + trainNumber + " is running.");
}
